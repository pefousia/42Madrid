#include <unistd.h>

void	ft_print_comb(void)
{
	int	cen;
	int	dec;
	int	uni;
	int	i;

	i = 0;
	while (i <= 789)
	{
		cen = ((i / 100) + 48);
		dec = (((i % 100) / 10) + 48);
		uni = (((i % 100) % 10) + 48);
		if (cen < dec && dec < uni)
		{
			write (1, &cen, 1);
			write (1, &dec, 1);
			write (1, &uni, 1);
			if (i < 789)
			{
				write(1, ", ", 2);
			}
		}
		i++;
	}
}
